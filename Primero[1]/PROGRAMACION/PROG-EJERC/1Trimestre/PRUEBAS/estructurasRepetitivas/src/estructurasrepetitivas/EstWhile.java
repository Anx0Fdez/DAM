package estructurasrepetitivas;

public class EstWhile {
    // Codifica un programa que amose 5 veces DAM1
    
    public void amosarWhile(){
        int i = 0;
        
        while(i<7){
           System.out.println("DAM 1 --> " + (i+1));           
           i++ ;
        }
           System.out.println("Saimos do bucle");
        
    }
    
}
