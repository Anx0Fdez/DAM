public class Producto {
    private String code; // Código do produto.
    private String description; // Descrición do produto.
    private int price; // Prezo do produto.

    public String getCode() {
        return code; // Obtén o código do produto.
    }

    public void setCode(String code) {
        this.code = code; // Establece o código do produto.
    }

    public String getDescription() {
        return description; // Obtén a descrición do produto.
    }

    public void setDescription(String description) {
        this.description = description; // Establece a descrición do produto.
    }

    public int getPrice() {
        return price; // Obtén o prezo do produto.
    }

    public void setPrice(int price) {
        this.price = price; // Establece o prezo do produto.
    }
}