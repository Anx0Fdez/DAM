
package exemp_condicionais;


public class CondicionalSimple {
        public void VerConAnidados (int num1, int num2) {
            if(num1>num2) {
                System.out.println(num1 + " e maior que " + num2);
                        }

            else if (num1 == num2){
                System.out.println(num1 + " e igual que " + num2);
                        }   
            else {
                System.out.println(num1 + " e menor que " + num2);
                        }           

         }            
}
